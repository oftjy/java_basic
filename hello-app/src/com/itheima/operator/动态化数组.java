package com.itheima.operator;

public class 动态化数组 {
    public static void main(String[] args) {
//        定义数组的时候只确定元素的类型和数组的长度，之后再存入数据
        int[] arr = new int[3];

//        动态初始化：只指定长度后期赋值
//        静态初始化：开始就存入元素
//        两种写法独立，不可混用
//        int [] arrs = new int[3]{1,2,3};//混用就会报错

//        动态初始化之后的默认值：
//        byte,short,int,char,long类型默认都是0
//        float，double都是0.0
//        boolean类型数组元素的默认值都是false，string类型的数组元素的默认值是null

//
    }
}
